    function t(key) {
        if (translations[key]) return translations[key][currentLang] || translations[key]['ar'];
        return key;
    }

    function applyTranslations() {
        document.querySelectorAll('[data-i18n]').forEach(function(el) {
            var key = el.getAttribute('data-i18n');
            el.textContent = t(key);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
            var key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = t(key);
        });
        var toggle = document.getElementById('lang-toggle');
        if (toggle) toggle.textContent = currentLang === 'he' ? 'AR' : 'HE';
        var adminSelect = document.querySelector('select[onchange="setLang(this.value)"]');
        if (adminSelect) adminSelect.value = currentLang;
        if (currentLang === 'he') {
            document.documentElement.dir = 'rtl';
            document.documentElement.lang = 'he';
            replaceTextInDOM();
        } else {
            document.documentElement.dir = 'rtl';
            document.documentElement.lang = 'ar';
        }
        document.documentElement.classList.add('lang-ready');
    }

    function replaceTextInDOM() {
        var excludeTags = { SCRIPT:1, STYLE:1, INPUT:1, TEXTAREA:1, SELECT:1, OPTION:1 };
        var walker = document.createTreeWalker(document.body, 4, function(n) { return excludeTags[n.parentElement?.nodeName] ? 2 : 1; }, false);
        var nodes = [];
        while (walker.nextNode()) nodes.push(walker.currentNode);
        var keys = Object.keys(AR_TO_HE).sort(function(a, b) { return b.length - a.length; });
        nodes.forEach(function(node) {
            var text = node.nodeValue;
            if (text && text.trim()) {
                var newText = text;
                for (var i = 0; i < keys.length; i++) {
                    var ar = keys[i];
                    if (newText.indexOf(ar) !== -1) {
                        newText = newText.split(ar).join(AR_TO_HE[ar]);
                    }
                }
                if (newText !== text) node.nodeValue = newText;
            }
        });

        document.querySelectorAll('img[alt]').forEach(function(img) {
            if (img.alt in AR_TO_HE) img.alt = AR_TO_HE[img.alt];
        });
        document.querySelectorAll('[title]').forEach(function(el) {
            var t = el.getAttribute('title');
            if (t && t in AR_TO_HE) el.setAttribute('title', AR_TO_HE[t]);
        });
        document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(function(el) {
            var p = el.placeholder;
            if (p && p in AR_TO_HE) el.placeholder = AR_TO_HE[p];
        });
        document.querySelectorAll('select option').forEach(function(opt) {
            var t = opt.textContent;
            if (t && t in AR_TO_HE) opt.textContent = AR_TO_HE[t];
        });
    }

    function detectLang() {
        var storedLang = localStorage.getItem('auraLang');
        if (storedLang === 'he' || storedLang === 'ar') {
            currentLang = storedLang;
            applyTranslations();
            return;
        }
        currentLang = 'he';
        localStorage.setItem('auraLang', 'he');
        applyTranslations();

        try {
            var xhr = new XMLHttpRequest();
            xhr.open('GET', 'https://ipapi.co/json/');
            xhr.timeout = 3000;
            xhr.onload = function() {
                if (xhr.status === 200) {
                    try {
                        var data = JSON.parse(xhr.responseText);
                        if (data.country_code) localStorage.setItem('auraDetectedCountry', data.country_code);
                    } catch(e) {}
                }
            };
            xhr.onerror = function() {
                try {
                    var x2 = new XMLHttpRequest();
                    x2.open('GET', 'https://ip-api.com/json/');
                    x2.timeout = 3000;
                    x2.onload = function() {
                        if (x2.status === 200) {
                            try { var d = JSON.parse(x2.responseText); if (d.countryCode) localStorage.setItem('auraDetectedCountry', d.countryCode); } catch(e) {}
                        }
                    };
                    x2.send();
                } catch(e) {}
            };
            xhr.send();
        } catch(e) {}
    }

    detectLang();
    window.__ = t;
    window.setLang = function(lang) {
        currentLang = lang;
        localStorage.setItem('auraLang', lang);
        applyTranslations();
    };
    window.applyTranslations = applyTranslations;
    window.currentLang = function() { return currentLang; };
